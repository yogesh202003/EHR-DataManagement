class DashboardView(TemplateView):
    template_name = 'management/index.html'

    def dispatch(self, request, *args, **kwargs):
        if not request.user.is_authenticated:
            return redirect('login')
        return super().dispatch(request, *args, **kwargs)

    def get_context_data(self, **kwargs):
        context = super().get_context_data(**kwargs)
        user = self.request.user
        if user.is_authenticated:
            if user.role == 'Admin' or user.is_staff:
                context['role'] = 'Admin'
                context['total_patients'] = PersonDetail.objects.count()
                context['total_appointments'] = Appointment.objects.count()
                from decimal import Decimal
                context['total_revenue'] = Invoice.objects.filter(status='Paid').aggregate(Sum('total_amount'))['total_amount__sum'] or Decimal('0.00')
                context['pending_revenue'] = Invoice.objects.filter(status='Unpaid').aggregate(Sum('total_amount'))['total_amount__sum'] or Decimal('0.00')

                total_billed = context['total_revenue'] + context['pending_revenue']
                if total_billed > 0:
                    context['collection_rate'] = (context['total_revenue'] / total_billed) * 100
                else:
                    context['collection_rate'] = 0

                context['recent_appointments'] = Appointment.objects.order_by('-appointment_date')[:5]

                context['blood_summary'] = PersonDetail.objects.values('blood_group').annotate(count=Count('blood_group'))

                # Revenue Chart Data (Last 30 days)
                revenue_rows = Invoice.objects.filter(
                    status='Paid',
                    issued_date__isnull=False,
                    issued_date__gte=timezone.now() - timezone.timedelta(days=30),
                ).order_by('issued_date').values_list('issued_date', 'total_amount')

                revenue_totals = OrderedDict()
                for issued_date, total_amount in revenue_rows:
                    local_date = timezone.localtime(issued_date).date()
                    revenue_totals[local_date] = revenue_totals.get(local_date, 0) + float(total_amount or 0)

                context['revenue_labels'] = [day.strftime('%d %b') for day in revenue_totals.keys()]
                context['revenue_values'] = list(revenue_totals.values())
                context['pending_revenue'] = Invoice.objects.filter(status='Unpaid').aggregate(Sum('total_amount'))['total_amount__sum'] or 0
            elif user.role == 'Doctor':
                context['role'] = 'Doctor'
                doctor = Doctor.objects.get(user=user)
                context['doctor'] = doctor
                context['my_appointments'] = Appointment.objects.filter(doctor=doctor).order_by('appointment_date')
                context['pending_appointments'] = context['my_appointments'].filter(status='Scheduled').count()
                context['today_appointments'] = context['my_appointments'].filter(appointment_date__date=timezone.now().date())
            else:
                context['role'] = 'Patient'
                if hasattr(user, 'person') and user.person:
                    context['patient'] = user.person
                    context['appointments'] = Appointment.objects.filter(patient=user.person).order_by('-appointment_date')
                    context['reports'] = MedicalReport.objects.filter(patient=user.person).order_by('-report_date')
                    context['invoices'] = Invoice.objects.filter(patient=user.person).order_by('-issued_date')
        return context


@csrf_exempt
def symptom_checker(request):
    if request.method == 'POST':
        try:
            symptoms = request.POST.get('symptoms', '').strip()
            if not symptoms:
                return JsonResponse({'error': 'Symptoms cannot be empty'}, status=400)

            disease, prob, spec, med, dosage = predict_disease_from_symptoms(symptoms)

            if request.user.is_authenticated:
                SymptomCheck.objects.create(
                    patient=request.user,
                    symptoms=symptoms,
                    predicted_disease=disease,
                    probability=prob,
                    suggested_specialization=spec,
                    medication=med,
                    dosage_instructions=dosage
                )
            return JsonResponse({
                'disease': disease,
                'probability': prob,
                'specialization': spec,
                'medication': med,
                'dosage': dosage
            })
        except Exception as e:
            import logging
            logging.exception('Symptom checker error')
            return JsonResponse({'error': f'Server error: {str(e)}'}, status=500)
    return render(request, 'management/symptom_checker.html')


def telemedicine_room(request, appointment_id):
    appointment = get_object_or_404(Appointment, id=appointment_id)
    if request.method == 'POST':
        content = request.POST.get('content')
        if content:
            Message.objects.create(appointment=appointment, sender=request.user, content=content)
            return redirect('telemedicine_room', appointment_id=appointment_id)

    messages = appointment.messages.all().order_by('timestamp')
    return render(request, 'management/telemedicine.html', {
        'appointment': appointment,
        'messages': messages
    })


