@csrf_exempt
def ask_ai(request):
    # This endpoint supports both JSON body and form-encoded POSTs.
    if request.method != 'POST':
        return JsonResponse({'answer': 'Invalid request method.'}, status=405)

    question = ''
    try:
        if request.body:
            try:
                data = json.loads(request.body)
                question = data.get('question', '')
            except json.JSONDecodeError:
                question = request.POST.get('question', '')
        else:
            question = request.POST.get('question', '')
    except Exception as e:
        # Defensive: ensure we always have a string
        question = ''

    if not question:
        return JsonResponse({'answer': 'Please ask a question.'})

    # Use the full local dataset so rule-based fallback answers are accurate.
    persons = PersonDetail.objects.order_by('-created_at')

    try:
        answer = get_ai_response(question, persons)
    except Exception as e:
        # If the AI call raises, log and fall back
        import logging
        logging.exception('AI call failed')
        answer = None

    # If AI is unavailable or returned an error string, provide a lightweight local fallback
    if not answer or isinstance(answer, str) and (
        'missing' in answer.lower() or 'error' in answer.lower() or 'connecting' in answer.lower() or 'api key' in answer.lower()
    ):
        # Simple fallback: handle a few common database questions locally
        qa = question.lower()
        # blood group lookup: "blood group of <name>"
        if 'blood group of' in qa:
            name = qa.split('blood group of')[-1].strip()
            person = PersonDetail.objects.filter(full_name__icontains=name).first()
            if person:
                answer = f"{person.full_name}'s blood group is {person.blood_group or 'Unknown'}."
            else:
                answer = "I couldn't find that person in the database."
        elif qa.startswith('who is') or qa.startswith('who was'):
            name = qa.split('who is')[-1].split('who was')[-1].strip()
            person = PersonDetail.objects.filter(full_name__icontains=name).first()
            if person:
                answer = f"{person.full_name} — {person.gender or 'N/A'}, {person.city or 'Unknown city'}; contact: {person.mobile_number or 'N/A'}."
            else:
                answer = "I couldn't find that person in the database."
        elif 'which people live in' in qa or 'who live in' in qa:
            # extract city token
            tokens = qa.split()
            city = tokens[-1]
            people = PersonDetail.objects.filter(city__icontains=city)[:10]
            if people:
                names = ', '.join([p.full_name for p in people])
                answer = f"People found in {city}: {names}."
            else:
                answer = f"No people found in {city}."
        elif 'show me all active' in qa or 'active records' in qa:
            people = PersonDetail.objects.filter(status__iexact='Active')[:20]
            if people:
                answer = 'Active records: ' + ', '.join([p.full_name for p in people])
            else:
                answer = 'No active records found.'
        else:
            # generic fallback message
            answer = answer or "AI service is currently unavailable — please try again later or check server logs."

    return JsonResponse({'answer': answer})


