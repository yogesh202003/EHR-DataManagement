    draw.text((110, height - 160), "Generated by EHR Data", fill='#475569', font=body_font)
    draw.text((110, height - 120), "Computer-generated billing invoice image", fill='#475569', font=body_font)

    buffer = BytesIO()
    image.save(buffer, format='PNG')
    buffer.seek(0)
    return buffer


