        response = super().form_valid(form)
        full_name = form.cleaned_data.get('full_name', '').strip() or self.object.username
        person = PersonDetail.objects.create(
            full_name=full_name,
            email=self.object.email,
            mobile_number=form.cleaned_data.get('phone', ''),
        )
        self.object.person = person
        self.object.save()
        login(self.request, self.object)
        return response


