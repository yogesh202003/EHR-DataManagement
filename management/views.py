class BillingListView(TemplateView):
    template_name = 'management/billing_v2.html'

    def get_context_data(self, **kwargs):
        context = super().get_context_data(**kwargs)
        user = self.request.user
        if user.is_authenticated:
            if user.role == 'Admin' or user.is_staff:
                context['invoices'] = Invoice.objects.all().order_by('-issued_date')
                context['patients'] = PersonDetail.objects.all()
                context['total_revenue'] = (Invoice.objects.filter(status='Paid').aggregate(Sum('total_amount'))['total_amount__sum'] or 0)
                context['pending_revenue'] = (Invoice.objects.filter(status='Unpaid').aggregate(Sum('total_amount'))['total_amount__sum'] or 0)
                context['processed_today'] = Payment.objects.filter(payment_date__date=timezone.now().date()).count()
            else:
                # Patient Specific View
                if hasattr(user, 'person') and user.person:
                    patient_invoices = Invoice.objects.filter(patient=user.person).order_by('-issued_date')
                    context['invoices'] = patient_invoices
                    context['total_revenue'] = (patient_invoices.filter(status='Paid').aggregate(Sum('total_amount'))['total_amount__sum'] or 0)
                    context['pending_revenue'] = (patient_invoices.filter(status='Unpaid').aggregate(Sum('total_amount'))['total_amount__sum'] or 0)
                else:
                    context['invoices'] = []
        return context

    def post(self, request, *args, **kwargs):
        action = request.POST.get('action')
        try:
            if action == 'create_invoice':
                patient_id = request.POST.get('patient_id')
                amount = request.POST.get('amount')
                if patient_id and amount:
                    patient = PersonDetail.objects.get(id=patient_id)
                    Invoice.objects.create(
                        patient=patient,
                        total_amount=amount,
                        status='Unpaid'
                    )
            elif action == 'make_payment':
                invoice_id = request.POST.get('invoice_id')
                amount = request.POST.get('amount')
                method = request.POST.get('payment_method', 'Online')
                if invoice_id and amount:
                    invoice = Invoice.objects.get(id=invoice_id)
                    Payment.objects.create(
                        invoice=invoice,
                        amount_paid=amount,
                        payment_method=method
                    )
                    invoice.status = 'Paid'
                    invoice.save()
        except Exception as e:
            import logging
            logging.error(f"Billing Final Execution Error: {str(e)}")

        from django.http import HttpResponseRedirect
        return HttpResponseRedirect('/billing/')


