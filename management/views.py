def user_management(request):
    """Admin-only view to list all users and handle user deletion."""
    if not request.user.is_authenticated:
        return redirect('login')
    if request.user.role != 'Admin' and not request.user.is_staff:
        return redirect('dashboard')

    if request.method == 'POST':
        user_id = request.POST.get('user_id')
        if user_id:
            try:
                target_user = User.objects.get(id=user_id)
                # Safety: prevent deleting yourself or superusers
                if target_user.id == request.user.id:
                    messages.warning(request, "You cannot delete your own account.")
                elif target_user.is_superuser:
                    messages.warning(request, "Superuser accounts are protected and cannot be deleted.")
                else:
                    username = target_user.username
                    # Also delete linked PersonDetail if exists
                    if hasattr(target_user, 'person') and target_user.person:
                        target_user.person.delete()
                    target_user.delete()
                    messages.success(request, f'User "{username}" has been permanently removed.')
            except User.DoesNotExist:
                messages.error(request, "User not found.")
        return redirect('user_management')

    all_users = User.objects.select_related('person').order_by('-date_joined')
    context = {
        'users': all_users,
        'admin_count': all_users.filter(role='Admin').count() + all_users.filter(is_staff=True, role='').count(),
        'doctor_count': all_users.filter(role='Doctor').count(),
        'patient_count': all_users.filter(role='Patient').count(),
    }
    return render(request, 'management/user_management.html', context)
