def _draw_wrapped_text(draw, text, position, font, max_width, fill, line_spacing=8):
    x, y = position
    words = (text or '').split()
    if not words:
        return y

    line = []
    for word in words:
        test_line = " ".join(line + [word])
        bbox = draw.textbbox((x, y), test_line, font=font)
        if bbox[2] - bbox[0] <= max_width:
            line.append(word)
            continue
        draw.text((x, y), " ".join(line), font=font, fill=fill)
        y += (bbox[3] - bbox[1]) + line_spacing
        line = [word]

    if line:
        bbox = draw.textbbox((x, y), " ".join(line), font=font)
        draw.text((x, y), " ".join(line), font=font, fill=fill)
        y += (bbox[3] - bbox[1]) + line_spacing
    return y


def _build_invoice_pdf(invoice):
    buffer = BytesIO()
    pdf = canvas.Canvas(buffer, pagesize=A4)
    width, height = A4

    pdf.setFillColor(colors.HexColor('#0f172a'))
    pdf.rect(0, height - 55 * mm, width, 55 * mm, fill=1, stroke=0)
    pdf.setFillColor(colors.white)
    pdf.setFont("Helvetica-Bold", 24)
    pdf.drawString(20 * mm, height - 22 * mm, "EHR DATA INVOICE")
    pdf.setFont("Helvetica", 11)
    pdf.drawString(20 * mm, height - 30 * mm, "Healthcare Billing Statement")

    pdf.setFillColor(colors.HexColor('#0f172a'))
    pdf.setFont("Helvetica-Bold", 13)
    pdf.drawString(20 * mm, height - 72 * mm, f"Invoice #: INV-{invoice.id:04d}")
    pdf.setFont("Helvetica", 11)
    pdf.drawString(20 * mm, height - 80 * mm, f"Issue Date: {timezone.localtime(invoice.issued_date).strftime('%d %b %Y %I:%M %p') if invoice.issued_date else 'N/A'}")
    pdf.drawString(20 * mm, height - 87 * mm, f"Status: {invoice.status}")
    pdf.drawString(20 * mm, height - 94 * mm, f"Patient: {invoice.patient.full_name}")
    pdf.drawString(20 * mm, height - 101 * mm, f"Contact: {invoice.patient.mobile_number or 'N/A'}")
    pdf.drawString(20 * mm, height - 108 * mm, f"Email: {invoice.patient.email or 'N/A'}")
    pdf.drawString(20 * mm, height - 115 * mm, f"Address: {invoice.patient.address_line1 or 'Not provided'}")

    y = height - 138 * mm
    pdf.setFillColor(colors.HexColor('#eff6ff'))
    pdf.roundRect(18 * mm, y - 8 * mm, width - 36 * mm, 16 * mm, 4 * mm, fill=1, stroke=0)
    pdf.setFillColor(colors.HexColor('#1d4ed8'))
    pdf.setFont("Helvetica-Bold", 12)
    pdf.drawString(24 * mm, y - 1 * mm, "Billing Summary")

    pdf.setFillColor(colors.HexColor('#0f172a'))
    pdf.setFont("Helvetica", 11)
    pdf.drawString(24 * mm, y - 15 * mm, f"Consultation / Services")
    pdf.drawRightString(width - 24 * mm, y - 15 * mm, f"Rs. {invoice.total_amount}")
    pdf.drawString(24 * mm, y - 24 * mm, f"Payments Received")
    paid_amount = sum((payment.amount_paid for payment in invoice.payments.all()), Decimal('0.00'))
    pdf.drawRightString(width - 24 * mm, y - 24 * mm, f"Rs. {paid_amount:.2f}")
    pdf.drawString(24 * mm, y - 33 * mm, f"Outstanding")
    outstanding = max(Decimal('0.00'), invoice.total_amount - paid_amount)
    pdf.drawRightString(width - 24 * mm, y - 33 * mm, f"Rs. {outstanding:.2f}")

    if invoice.appointment:
        pdf.drawString(24 * mm, y - 48 * mm, f"Appointment")
        pdf.drawRightString(
            width - 24 * mm,
            y - 48 * mm,
            timezone.localtime(invoice.appointment.appointment_date).strftime('%d %b %Y %I:%M %p')
        )

    pdf.setStrokeColor(colors.HexColor('#cbd5e1'))
    pdf.line(20 * mm, 28 * mm, width - 20 * mm, 28 * mm)
    pdf.setFont("Helvetica", 9)
    pdf.setFillColor(colors.HexColor('#475569'))
    pdf.drawString(20 * mm, 20 * mm, "Generated by EHR Data billing module")
    pdf.drawRightString(width - 20 * mm, 20 * mm, "This is a computer-generated invoice")

    pdf.showPage()
    pdf.save()
    buffer.seek(0)
    return buffer


def _build_invoice_image(invoice):
    width, height = 1400, 1900
