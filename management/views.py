def analytics_dashboard(request):
    # Clinical Insights
    disease_trends = SymptomCheck.objects.values('predicted_disease').annotate(count=Count('predicted_disease')).order_by('-count')[:5]

    # Operational Throughput (Last 7 days)
    visit_rows = Appointment.objects.filter(
        appointment_date__isnull=False,
        appointment_date__gte=timezone.now() - timezone.timedelta(days=7),
    ).order_by('appointment_date').values_list('appointment_date', flat=True)
    visit_totals = OrderedDict()
    for appointment_date in visit_rows:
        local_day = timezone.localtime(appointment_date).date()
        visit_totals[local_day] = visit_totals.get(local_day, 0) + 1

    # Inventory Health
    total_inventory = InventoryItem.objects.count()
    from django.db.models import F
    low_stock_count = InventoryItem.objects.filter(quantity__lte=F('threshold')).count()

    # Department Loading (Dynamic Real-time data)
    today_appointments = Appointment.objects.filter(appointment_date__date=timezone.now().date()).count()
    opd_load = min(100, int((today_appointments / 20) * 100)) if today_appointments > 0 else 10

    pharmacy_load = min(100, int((low_stock_count / max(1, total_inventory)) * 100)) if total_inventory > 0 else 0

    pending_reports = MedicalReport.objects.filter(status='Pending').count()
    lab_load = min(100, int((pending_reports / 10) * 100)) if pending_reports > 0 else 15

    dept_load = [
        {'name': 'OPD', 'load': opd_load},
        {'name': 'Radiology', 'load': 30},  # Baseline
        {'name': 'Pharmacy', 'load': pharmacy_load},
        {'name': 'Laboratory', 'load': lab_load}
    ]

    return render(request, 'management/analytics.html', {
        'disease_trends': list(disease_trends),
        'visit_labels': [day.strftime('%a') for day in visit_totals.keys()],
        'visit_counts': list(visit_totals.values()),
        'total_inventory': total_inventory,
        'low_stock_count': low_stock_count,
        'dept_load': dept_load,
        'total_patients': PersonDetail.objects.count(),
        'ai_run_count': SymptomCheck.objects.filter(created_at__gte=timezone.now() - timezone.timedelta(days=30)).count()
    })


def nearby_healthcare_api(request):
    lat = request.GET.get('lat')
    lon = request.GET.get('lon')

    if lat is None or lon is None:
        return JsonResponse({'error': 'Latitude and longitude are required.'}, status=400)

    try:
        providers = get_nearby_healthcare(lat, lon)
    except Exception as exc:
        return JsonResponse({'error': f'Unable to fetch providers: {exc}'}, status=500)

    return JsonResponse({'providers': providers})


